제15조(준거법 및 재판관할)<BR>
①회사와 회원 간 제기된 소송은 대한민국법을 준거법으로 합니다.<BR>
②본 약관의 일부 조항이 관할 법원 또는 현지 법령 등에 의해 무효 또는 시행 불가능하다고 판단 되더라도 본 약관의 잔여 부분은 유효하며, 어떠한 일이 있어도 무효화되지 않습니다.<BR>
③본 약관은 최초 한국어로 작성되었으며, 회원이 본 이용약관을 한국어가 아닌 다른 언어로 접할 시 회원은 한국어가 아닌 언어의 약관은 오직 회원의 편의를 위해 제공되었으며, 언어간 의미에 차이가 발생할 경우 한국어로 작성된 약관의 해석이 항상 우선함을 이해하고 동의합니다.<BR>
④회사와 회원 간 발생한 분쟁에 관한 소송은 서울중앙지방법원에 제소합니다.<BR>
